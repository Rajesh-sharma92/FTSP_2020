# Print all the numbers from 1 to 10 using condition in while loop

i=1
while i<=10:
    
    print(i)
    i=i+1


# Print all the numbers from 1 to 10 using while True loop

i=1
while(True):
        
    if i<=10:
        print(i)
        i=i+1

# Print all the even numbers from 1 to 10 using condition in while loop

